#!/bin/bash
# 批量添加文档导航

cd /root/.openclaw/workspace/projects/hu-hr-rights/docs

# 定义文档顺序和对应关系
declare -A nav_map=(
    ["02-工资与税务"]="01-劳动法核心条款:03-工作时间与休假"
    ["03-工作时间与休假"]="02-工资与税务:04-离职与赔偿"
    ["04-离职与赔偿"]="03-工作时间与休假:05-劳动纠纷处理"
    ["05-劳动纠纷处理"]="04-离职与赔偿:06-华人特殊场景"
    ["06-华人特殊场景"]="05-劳动纠纷处理:07-实用资源"
    ["07-实用资源"]="06-华人特殊场景:98-真实案例"
    ["98-真实案例"]="07-实用资源:99-使用示例"
    ["99-使用示例"]="98-真实案例:00-总览"
)

for doc in "${!nav_map[@]}"; do
    file="${doc}.md"
    if [ -f "$file" ]; then
        nav="${nav_map[$doc]}"
        prev="${nav%%:*}"
        next="${nav##*:}"
        
        # 在标题后添加导航
        sed -i "/^# /a\\\n---\\\n**📍 导航**: [← ${prev:3}](${prev}.html) | [返回总览](00-总览.html) | [${next:3} →](${next}.html)\\\n---" "$file"
        
        # 在文件末尾添加导航
        echo "" >> "$file"
        echo "---" >> "$file"
        echo "" >> "$file"
        echo "**📍 导航**: [← ${prev:3}](${prev}.html) | [返回总览](00-总览.html) | [${next:3} →](${next}.html)" >> "$file"
        echo "" >> "$file"
        echo "**📚 快速链接**: [劳动法](01-劳动法核心条款.html) | [工资税务](02-工资与税务.html) | [离职赔偿](04-离职与赔偿.html) | [真实案例](98-真实案例.html)" >> "$file"
        
        echo "✅ 已添加导航: $file"
    fi
done

echo ""
echo "=========================================="
echo "所有文档导航已添加！"
echo "=========================================="
